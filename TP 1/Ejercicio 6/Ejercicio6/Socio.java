package Ejercicio6;

public class Socio {
    private String nombre;
    private int edad;
    private boolean reprClub;

    public String getNombre() {
        return nombre;
    }
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public int getEdad() {
        return edad;
    }
    public void setEdad(int edad) {
        this.edad = edad;
    }
    public boolean isReprClub() {
        return reprClub;
    }
    public void setReprClub(boolean reprClub) {
        this.reprClub = reprClub;
    }
}
